def add_word():
    """
    This function allows the user to add a new word to the list of word choices.
    The new word is appended to the word_list and is returned.
    """

    global WORD_LIST

    new_word = input("Please enter a word to add: ").lower()
    if new_word in WORD_LIST:
        print(new_word, ' already exist in our Hangman word database.')
    else:
        WORD_LIST.append(new_word)
        print("The word '" + new_word +
              "' has been added to the list of word choices.")
        sort_word_list = sorted(WORD_LIST)
        return sort_word_list
